# Função onde o jogo ocorre
def pacman():
    # criando o objeto pacman
    size = TELA_Y // 30

    pacman = Pacman(size)
    cenario = Cenario(size)

    pg.init()
    tela = pg.display.set_mode((TELA_X, TELA_Y), 0)
    jogando = True

    # loop principal do jogo
    while jogando:

        tela.fill(PRETO)
        cenario.pintar(tela)
        pacman.pintar(tela)
        pacman.calcular_regras()
        pg.display.update()
        pg.time.delay(100)

        # Captura de eventos
        eventos = pg.event.get()
        for e in eventos:
            if e.type == pg.QUIT:
                exit()
        pacman.processar_eventos(eventos)


